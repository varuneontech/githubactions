package com.example.eon.crm.enums;

public enum EmailModule {
    PROMOTIONAL,   // Emails related to marketing and promotional offers
    TRANSACTIONAL, // Emails related to specific transactions, such as receipts or confirmations
    NOTIFICATION,  // Emails for notifying users about important events or updates
    NEWSLETTER,    // Regular newsletters sent to subscribers
    REMINDER,      // Emails that serve as reminders, such as upcoming appointments or renewals
    ALERT          // Emails that alert users to specific actions or issues, like account security alerts
}
